char	*ft_strncpy(char *dest, const char *src, unsigned int n)
{
	unsigned int	i;
	char			*dest_start;

	i = 0;
	dest_start = dest;
	while (i < n && src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	while (i < n)
	{
		dest[i] = '\0';
		i++;
	}
	return (dest_start);
}
